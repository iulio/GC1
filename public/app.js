const slides = Array.from(document.querySelectorAll("[data-slide]"));
const prev = document.getElementById("prevSlide");
const next = document.getElementById("nextSlide");
const count = document.getElementById("slideCount");
const start = document.getElementById("startPresentation");
const contactForm = document.getElementById("contactForm");
const formStatus = document.getElementById("formStatus");
const languageModal = document.getElementById("languageModal");
const languageTrigger = document.getElementById("languageTrigger");
const languageChoices = document.querySelectorAll("[data-language-choice]");
const metaDescription = document.getElementById("metaDescription");
const cookieBanner = document.getElementById("cookieBanner");
const cookieAccept = document.getElementById("cookieAccept");
const cookieEssential = document.getElementById("cookieEssential");

let current = 0;
let currentLanguage = "en";

function translate(key) {
  return translations[currentLanguage][key] || translations.en[key] || key;
}

function getSavedLanguage() {
  try {
    return localStorage.getItem("global-consult-language");
  } catch (error) {
    return null;
  }
}

function saveLanguage(language) {
  try {
    localStorage.setItem("global-consult-language", language);
  } catch (error) {
    // The language still applies for this visit if storage is unavailable.
  }
}

function getCookieConsent() {
  try {
    return localStorage.getItem("global-consult-cookie-consent");
  } catch (error) {
    return null;
  }
}

function saveCookieConsent(value) {
  try {
    localStorage.setItem("global-consult-cookie-consent", value);
  } catch (error) {
    // Consent still applies for this visit if storage is unavailable.
  }
}

function applyLanguage(language) {
  currentLanguage = translations[language] ? language : "en";
  document.documentElement.lang = currentLanguage;
  document.title = translate("meta.title");
  metaDescription.setAttribute("content", translate("meta.description"));

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    element.textContent = translate(element.dataset.i18n);
  });
}

function openLanguageModal() {
  languageModal.hidden = false;
  document.body.classList.add("modal-open");
}

function closeLanguageModal() {
  languageModal.hidden = true;
  document.body.classList.remove("modal-open");
  showCookieBannerIfNeeded();
}

function showCookieBannerIfNeeded() {
  if (!cookieBanner || getCookieConsent()) return;
  cookieBanner.hidden = false;
}

function closeCookieBanner(choice) {
  saveCookieConsent(choice);
  cookieBanner.hidden = true;
}

function updateCount() {
  count.textContent = `${current + 1} / ${slides.length}`;
}

function goToSlide(index) {
  current = (index + slides.length) % slides.length;
  slides[current].scrollIntoView({ behavior: "smooth", block: "start" });
  updateCount();
}

prev.addEventListener("click", () => goToSlide(current - 1));
next.addEventListener("click", () => goToSlide(current + 1));
start.addEventListener("click", () => goToSlide(1));
languageTrigger.addEventListener("click", openLanguageModal);

languageChoices.forEach((button) => {
  button.addEventListener("click", () => {
    applyLanguage(button.dataset.languageChoice);
    saveLanguage(currentLanguage);
    closeLanguageModal();
  });
});

if (cookieAccept) {
  cookieAccept.addEventListener("click", () => closeCookieBanner("accepted"));
}

if (cookieEssential) {
  cookieEssential.addEventListener("click", () => closeCookieBanner("essential"));
}

window.addEventListener("keydown", (event) => {
  if (event.key === "Escape" && getSavedLanguage()) {
    closeLanguageModal();
    return;
  }

  const tag = event.target.tagName;
  if (tag === "INPUT" || tag === "TEXTAREA" || tag === "SELECT") return;
  if (event.key === "ArrowRight" || event.key === "PageDown") goToSlide(current + 1);
  if (event.key === "ArrowLeft" || event.key === "PageUp") goToSlide(current - 1);
});

const observer = new IntersectionObserver(
  (entries) => {
    const active = entries
      .filter((entry) => entry.isIntersecting)
      .sort((a, b) => b.intersectionRatio - a.intersectionRatio)[0];

    if (!active) return;
    current = slides.indexOf(active.target);
    updateCount();
  },
  { threshold: [0.45, 0.6, 0.8] }
);

slides.forEach((slide) => observer.observe(slide));
updateCount();

contactForm.addEventListener("submit", async (event) => {
  event.preventDefault();

  const submit = contactForm.querySelector("button[type='submit']");
  const formData = new FormData(contactForm);
  formStatus.className = "form-status";
  formStatus.textContent = translate("form.sending");
  submit.disabled = true;

  try {
    const response = await fetch(contactForm.action, {
      method: "POST",
      body: new URLSearchParams(formData),
      headers: {
        "Content-Type": "application/x-www-form-urlencoded",
      },
    });

    if (!response.ok) {
      throw new Error("Message could not be sent.");
    }

    contactForm.reset();
    formStatus.className = "form-status success";
    formStatus.textContent = translate("form.success");
  } catch (error) {
    formStatus.className = "form-status error";
    formStatus.textContent = translate("form.error");
  } finally {
    submit.disabled = false;
  }
});

const savedLanguage = getSavedLanguage();
applyLanguage(savedLanguage || "ro");
if (!savedLanguage) {
  openLanguageModal();
} else {
  showCookieBannerIfNeeded();
}
